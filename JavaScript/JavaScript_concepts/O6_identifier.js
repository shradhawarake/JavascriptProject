// identifiers
var a = 10; // 'name' is an identifier
a=20;
console.log(a); // Output: 20

//a is an identifier
// 10 is a literal
// = is an operator

// rules for identifiers
// 1. An identifier can only contain letters, digits, underscores, and dollar signs.
// 2. An identifier cannot start with a digit.
// 3. An identifier cannot be a reserved keyword in JavaScript.
// 4. An identifier is case-sensitive.
// 5. An identifier cannot contain spaces.
// 6. An identifier cannot be the same as a built-in object or function name in JavaScript.

// valid identifiers
var myVariable = 10;
var _myVariable = 20;
var $myVariable = 30;
var myVariable123 = 40;
